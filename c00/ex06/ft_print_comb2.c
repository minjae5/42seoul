#include <stdio.h>
#include <unistd.h>

void	ft_show(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	b = 1;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			ft_show((a / 10) + '0');
			ft_show((a % 10) + '0');
			write(1, " ", 1);
			ft_show((b / 10) + '0');
			ft_show((b % 10) + '0');
			if (a != 98 || b != 99)
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
